    public void listado(List<Alumno> alumnos) {
        // Recorrer la lista y actualizar la pantalla

        String s = "Alumnos\n";
        s += "-------\n\n";

        for (Alumno alumno : alumnos) {
            s += alumno + "\n";
        }

        s += "\nTotal: " + alumnos.size();

        jTextArea1.setText(s);
    }

