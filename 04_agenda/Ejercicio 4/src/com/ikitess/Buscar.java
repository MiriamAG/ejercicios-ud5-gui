    private void buscarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBotonActionPerformed

        if (comboBox.getSelectedIndex() == 0) {
            String text = buscarText.getText();
            //Recorrer array de personas y comparar el parametro pasado y mostrar el que coincida.
            //metodo en agenda
            Persona p = agenda.recorrerYBuscarNombre(text);
            //Pasar persona to string a resultado
            resultadoText.setText(p.toString());
        } else if (comboBox.getSelectedIndex() == 1) {
            String text = buscarText.getText();

            Persona p = agenda.recorrerYBuscarTelefono(text);

            if( p != null ) { 
                resultadoText.setText(p.toString());
            } else {
                resultadoText.setText("No se han encontrado resultados...");
            }
            
        } else if (comboBox.getSelectedIndex() == 2) {
            String text = buscarText.getText();

            Persona p = agenda.recorrerYBuscarEmail(text);
            resultadoText.setText(p.toString());

        }
    }//GEN-LAST:event_buscarBotonActionPerformed

