    private void verActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verActionPerformed
        if (ver_c == null) {
            ver_c = new Ver();
            ver_c.setAgenda(this);
        }
        System.out.println(personas.size());

        if (personas.size() == 0) {
            ver_c.setTextDefault();
            ver_c.setPosicion(0);
        }
        if (personas.size() > 0) {
            ver_c.setPosicion(0);
            ver_c.pasarMax(personas.size());
            ver_c.setTextDCHA();
        } else {
            ver_c.setPosicion(0);
        }

        ver_c.setVisible(true);


    }//GEN-LAST:event_verActionPerformed

